package com.magicare.smartnurse.bean;

import java.io.Serializable;
import java.util.List;

public class TVAreaActiveEntity implements Serializable{
	
	private static final long serialVersionUID = 1L;
	private int alarm_num;
	private List<AreaActiveBean> area_list;
	
	public int getAlarm_num(){
		return alarm_num;
	}
	
	public void setAlarm_num(int alarm_num){
		this.alarm_num = alarm_num;
	}
	
	public List<AreaActiveBean> getArea_list(){
		return area_list;
	}
	
	public void setArea_list(List<AreaActiveBean> area_list){
		this.area_list = area_list;
	}
	
	@Override
	public String toString() {
		return "TVAreaActiveEntity [alarm_num=" + alarm_num + ", area_list=" + area_list + "]";
	}	
}
